			<header class="section-header">
				<h2>Initial Token Allocation</h2>
				<hr>
				<p class="lead">BitShares 2.0 was created on 13th of October 2015 by the community and block producers of BitShares 0.9 who decided to start a new token with a distribution identical to where 0.9 had evolved. Based on code developed with private resources, and given to the world for anyone to use under the MIT license. The previous BitShares 0.9 Blockchain was abandoned by the community who had the option to continue that chain but declined to do so.</p>
				<p class="lead">In the genesis block of the BitShares Blockchain, a total of 2,412,042,197.37963 BTS have been distributed to individual keys accordingly. Original holders can still claim these BTS by proving ownership of the corresponding private key.</p>
				<p class="lead">The BTS token comes with a limited supply that is different from circulating (liquid) supply. The max supply placed on the blockchain is 3,600,570,502.10207 BTS. This number can never change. The difference of roughly 1.1B initial tokens was set aside for future project funding and rewarding block producers and is only accessible with approval by the BTS holders through the worker system. This so-called working budget is also often referred to as reserves. </p>
				<p class="lead">It is worth noting that revenues made from transactions fees are not shared with holders of BTS but instead go back into the working budget to further allow future development. There is no reward for holding the core BTS token in any way.</p>
			</header>
		</section>
		<section class="section pt-80 pb-35">
			<header class="section-header">
				<h2>Working Budget</h2>
				<hr>
				<p class="lead">The difference between max supply and circulating supply is called the Working Budget often referred to as the reserve pool.</p><br>
				<p class="lead">The BitShares Blockchain has a daily budget to use for development. This budget has a hard-coded upper limit of:</p>
				<br>
				<p class="lead"><em>Total funds in the working budget / 2924</em></p>
				<br><p class="lead">From this daily budget, block production, as well as project funding, is made. The BTS holders have the choice and need to approve BTS tokens leaving the working budget by voting on workers.</p>
			</header>
		</section>
		<section class="section pt-80 pb-35">
			<header class="section-header">
				<h2>Block Production (Witnesses)</h2>
				<hr>
				<p class="lead">Block production comes at the cost of running and maintaining equipment. The BitShares Blockchain acknowledges this fact by rewarding block producers in core BTS tokens per produced block. Depending on the valuation of BTS, the committee can modify the amount of BTS paid per block. As of Q1/2018, current block reward is 1 BTS. Those BTS come from the working budget.</p>
		</header>
		</section>
		<section class="section pt-80 pb-35">
			<header class="section-header">
				<h2>Project Funding (Workers)</h2>
				<hr>
				<p class="lead">A certain amount of the available daily tokens can be allocated to make development possible through workers. Anyone can set up a worker proposal on the BitShares Blockchain, and ask for a regular allowance in BTS. If enough weight in BTS holders approves, the funds are set aside from the daily budget. A Soft-limit defines the maximum amount of the budget that is given to the authorized workers daily. Consequently, those workers that have received more votes from BTS holders will receive funds first. This means that workers, even if approved, may not be funded if the daily reserve pool is exhausted. Workers continuously stand under the scrutiny of the BTS holders who can disapprove (e.g., fire) workers that do not deliver.</p>
			</header>
		</section>
		<section class="section pt-80 pb-35">
			<header class="section-header">
			<h2>Transaction Fees & Fee Schedule</h2>
			<hr>
			<p class="lead">Additional to block production and project funding which can drain tokens from the working budget, there are transaction fees paid by users of the BitShares Blockchain that go back into the working budget. As a consequence, the total amount of BTS in the working budget as well as the total in- and out-flow varies over time. However, if compared to most proof-of-work-based Blockchains that continuously reward a (more or less) fixed amount of tokens to miners, the BitShares Blockchain has a chance to have the working budget grow and consequently the circulating supply shrink. The former occurs when the total transactions fees outweigh the tokens used for block production and project funding.</p>
			<p class="lead">In contrast to other Blockchains, BitShares comes with fixed fees instead of a fee-based market. The fee schedule defines the cost required for each of the current 50+ blockchain features and operations available.</p>
			</header>
		</section>
		<section class="section pt-80 pb-35">
			<header class="section-header">
			<h2>Legality of the BTS token</h2>
			<hr>
			<p class="lead">It is worth noting that the BitShares Blockchain is not a traditional registered entity and has no seat. The core token BTS does not imply any ownership rights. The core token merely serves as a utility for governance, arranging transaction fees and operating other features implemented only on the BitShares Blockchain.</p>
			<br>
			</header>
