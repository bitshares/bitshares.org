				<h2>Serving utility and governance properties to its holders</h2>
				<hr>
				<p class="lead">BitShares Blockchain uses the DPoS method, meaning holders of the BTS core native token hold weight on decisions,  by the amount of BTS owned.</p>
				<p class="lead">To improve voter participation and simplify the life of BTS holders, voters can either vote directly or delegate voting power to so-called proxies.</p>
				<p class="lead">DPoS is similar to a representative democracy, where selected persons decide the course of action.  Leaders have to account for their actions, as core token holders can unelect them. However, the difference to representative democracy is that the community members (token holders) have their vote weighted by the amount of BTS that they own in their account. Undesired actions include favoritism and censorship.</p>
				<p class="lead">Desired actions include extensive, transparent reporting and technical competencies in timely block production. Leaders furthermore can provide value back to the community with infrastructure or tools, including free to use dApps. Others also offer the community marketing, informational blogs and websites, and reports or news on happenings around the BitShares eco-system. </p><br>
