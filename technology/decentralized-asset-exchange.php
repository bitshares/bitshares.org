    <h2>Roles of an Exchange</h2>
         <p>The roles that traditional exchanges perform today encompass:</p>

         <ol class="ml-45">
         <li>Receiving cryptocurrency and issuing IOUs.</li>
         <li>Receiving fiat and issuing IOUs.</li>
         <li>Processing an order book.</li>
         <li>Redeeming IOUs.</li>
         </ol>
<br />        
        <p>Each of these stages requires a high degree of trust and direct counterparty risk, because they involve an IOU from the exchange. To get the best liquidity and lowest spreads requires a large and active order book, and this means that most people gravitate toward a few core exchanges, leaving everyone exposed to the same counterparty risk.</p>

        <p>Moving money into or out of an exchange often incurs a significant time delay, which means that active traders must keep their funds on the exchange. This magnifies the amount of risk to
  users of the exchange. It also magnifies the risk to all users in the cryptocurrency ecosystem. Each large security breach results in significant sell pressure, from both the thief looking to
  cash in their loot, and from regular users hoping to sell before the thief.</p>
<br />

    <h4>Centralization Compromises Privacy</h4>
        <p>Cryptocurrencies depend upon a public ledger, which makes privacy challenging because everyone can see every transaction. Bitcoin gives every user one or more account numbers, and that
  gives many people a false sense of security. People assume as long as nobody knows your account number and you use a new account number with every transaction, nobody can tie
  all of your bitcoin transactions to your real identity.</p>

        <p>This is where the large centralized exchanges become a problem. In order to comply with government regulations, exchanges must know everyone they do business with. Since many bitcoin
  transactions flow through an exchange, the exchange learns who everyone is and can start to track who is doing business with whom.  <a href="https://www.cryptocoinsnews.com/coinbase-bringing-big-brother-bitcoin-accounts/" target="_blank">Coinbase have been known to close accounts</a> based upon who you do business with after withdrawing your bitcoins.</p>

        <p>In order to have even the slightest bit of privacy, the exchange functionality needs to be divided among hundreds of parties who are unlikely to collude to compromise identity. This is not
  economically practical today, because the exchange order book creates market incentives that naturally tend toward centralization in just a few exchanges with the vast majority of market share.
        </p>
<br />

    <h4>Separation of Powers</h4>
        <p>There is no reason why the same entity needs to be responsible for issuing IOUs and for processing the order book. It is only because these two roles are combined that we have a tendency
  toward centralization in the Bitcoin exchange space. If we want to create a decentralized exchange then the first step is to move the order book on to the blockchain where everyone can see it.
</p>

        <p>In this model, exchanges merely act as gateways that receive USD and issue GatewayUSD on the blockchain. Later, they receive GatewayUSD and then execute a wire transfer. They will make their
  money entirely on transaction fees and not from a percentage of market fees.</p>
	<!--  consider internal link in here for UIAs -->
<p>The blockchain allows users to trade, for example, BitstampUSD against BitfinexUSD, in order to easily move funds from one gateway to another. Users could even trade BitstampUSD against
  BitstampBTC or BitstampUSD vs BitfinexBTC.</p>

<p>Unfortunately, simply moving the order book to the blockchain is not enough, because the market will naturally centralize around a few <a href="../technology/user-issued-assets" target="_blank">gateway IOUs</a>
  and the markets for them. BitstampUSD is not fungible with BitfinexUSD because they have different trust profiles and regulatory considerations. Any of these IOUs are subject to default
  just like the IOUs that currently exist on the exchanges' internal databases. What we need to do is move the trust from individual issuers to the blockchain itself.</p>
<br />
<h4>Collateralized Blockchain IOUs</h4>
<p>The heart of BitShares is the SmartCoin system which enables the creation of 200% collateralized IOUs from the BitShares network. A BitUSD has all of the properties of Bitcoin combined with
  the price stability of the US dollar. At any point in time you can sell a BitUSD for at least 1 dollar worth of BTS. If at any time the value of the collateral falls below a certain point
  the blockchain will automatically buy back the BitUSD with a dollars worth of BTS.</p>

<p>When you hold BitUSD, the value of your holdings will remain pegged to the dollar so long as BitShares itself has reasonable volatility. Reasonable volatility in this case means that it can
  handle greater volatility than Bitcoin has ever seen in its lifetime. The price of BitShares would have to fall to less than 1/3 its starting price in less than 24 hours and then stay there.
  No legitimate, widely adopted cryptocurrency has ever seen that kind of price movement. This means that BitUSD is secure against just about everything but an unfixable software bug, and BitShares
  bug in the BitShares protocol itself. By the time BitShares matures to the level Bitcoin is at today, we expect the probability of that kind of bug to be similar to that of Bitcoin having such an event.</p>
<br />
<h4>Global Unified Order Book</h4>
<p>Once the market adopts BitUSD and BitBTC as more reliable and decentralized alternatives to BitstampUSD and BitfinexBTC, you will see the majority of trading volume move toward
  BitUSD vs BitBTC. The only time someone would want to move from BitUSD to BitstampUSD is when they are in the process of withdrawing to the traditional banking system.</p>

<p>The impact of a global unified order book is to end all arbitrage opportunities, minimize spreads, and maximize liquidity. By having the trades executed on the BitShares network, we also
  eliminate high-frequency trading and front running. High frequency trading and front running depend upon centralized exchanges with high volume and deep markets. When the vast majority of
  trading activity moves to a decentralized, trust-free exchange, the remaining centralized exchanges become much less appealing to high-frequency traders.</p>
<br />
<h4>BitUSD to USD Gateways</h4>
<p>Many gateways prefer the low-risk approach of one-for-one redemption and will simply allow the GatewayUSD to float against BitUSD with a small but variable spread in the market. Users then
  pay a small variable conversion cost as they exit from BitUSD to fiat USD through GatewayUSD.</p>

<p>On the other hand, many users will want a direct conversion from BitUSD to fiat USD. In this mode of operation, the gateway takes care of providing all of the liquidity within a fixed
  percentage transaction fee. The gateways then compete on offering the lowest possible spread.</p>

<p>Once this happens, BitUSD is effectively as good as USD with a small fixed conversion fee. This fee will likely be no more than the withdraw and deposit fees that current exchanges charge.
  At that point, BitShares will be a fully operational exchange with many banking partners providing high liquidity. A truly decentralized exchange will have been realized, and the original vision of BitShares completed.</p>
